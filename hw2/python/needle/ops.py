class LogSumExp(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, Z):
        ### BEGIN YOUR SOLUTION
        Z_max = array_api.max(Z, self.axes, keepdims=True)
        Z_max_broatcasted = array_api.broadcast_to(Z_max, Z.shape)
        
        Z_exp = array_api.exp(Z - Z_max_broatcasted)
        Z_exp_sum = array_api.sum(Z_exp, self.axes)
        return array_api.log(Z_exp_sum) + array_api.reshape(Z_max, Z_exp_sum.shape)
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
        Z = node.inputs[0]
        in_shape = Z.shape
        Z_max = array_api.amax(Z.cached_data, axis=self.axes, keepdims=True)
        Z_exp = exp(Z - Z_max)
        Z_exp_sum = summation(exp(Z - Z_max), self.axes)
        out_grad = out_grad / Z_exp_sum
        if self.axes is None:
            return (broadcast_to(out_grad, in_shape) * Z_exp,)
        else:
            shape = tuple(1 if idx in self.axes else s for idx, s in enumerate(in_shape))        
            return (broadcast_to(reshape(out_grad, shape), in_shape) * Z_exp,)
        ### END YOUR SOLUTION


def logsumexp(a, axes=None):
    return LogSumExp(axes=axes)(a)
